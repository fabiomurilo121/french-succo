    public static async Task SeedAsync(AppDbContext db, ILogger logger)
    {
        await db.Database.EnsureCreatedAsync();

        if (!await db.Stories.AnyAsync())
        {
            logger.LogInformation("Seeding stories data...");
            var stories = BuildStories();
            db.Stories.AddRange(stories);
            await db.SaveChangesAsync();
        }

        if (!await db.Words.AnyAsync())
        {
            logger.LogInformation("Seeding word dictionary ({Count} entries)...", BuildWordDictionary().Count);
            db.Words.AddRange(BuildWordDictionary());
            await db.SaveChangesAsync();
        }
        else
        {
            var existing = await db.Words.Select(w => w.Word).ToListAsync();
            var existingSet = new HashSet<string>(existing, StringComparer.OrdinalIgnoreCase);
            var toAdd = BuildWordDictionary()
                .Where(w => !existingSet.Contains(w.Word))
                .ToList();
            if (toAdd.Count > 0)
            {
                logger.LogInformation("Adding {Count} new word dictionary entries...", toAdd.Count);
                db.Words.AddRange(toAdd);
                await db.SaveChangesAsync();
            }
        }
    }

